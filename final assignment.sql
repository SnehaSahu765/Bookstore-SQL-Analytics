--Calculate the stock remaining after fulfilling all orders:
SELECT
	B.BOOK_ID,
	B.TITLE,
	B.STOCK,
	COALESCE(SUM(O.QUANTITY), 0) AS ORDER_QUANTITY,
	B.STOCK - COALESCE(SUM(O.QUANTITY), 0) AS REMAINING_QUANTITY
FROM
	BOOKS B
	LEFT JOIN ORDERS O ON B.BOOK_ID = O.BOOK_ID
GROUP BY
	B.BOOK_ID
ORDER BY
	B.BOOK_ID;

--Divide customers into 4 spending tiers (Quartiles) based on total purchase amount.
WITH
	CUSTOMER_SPENDING AS (
		SELECT
			C.CUSTOMER_ID,
			C.NAME,
			SUM(O.TOTAL_AMOUNT) AS TOTAL_SPENT
		FROM
			CUSTOMERS C
			JOIN ORDERS O ON C.CUSTOMER_ID = O.CUSTOMER_ID
		GROUP BY
			C.CUSTOMER_ID,
			C.NAME
	)
SELECT
	CUSTOMER_ID,
	NAME,
	TOTAL_SPENT,
	NTILE(4) OVER (
		ORDER BY
			TOTAL_SPENT DESC
	) AS SPENDING_TIER
FROM
	CUSTOMER_SPENDING;

--What is the monthly revenue trend and growth rate?
WITH
	MONTHLY_REVENUE AS (
		SELECT
			DATE_TRUNC('month', ORDER_DATE) AS MONTH,
			SUM(TOTAL_AMOUNT) AS REVENUE
		FROM
			ORDERS
		GROUP BY
			MONTH
	)
SELECT
	MONTH,
	REVENUE,
	LAG(REVENUE) OVER (
		ORDER BY
			MONTH
	) AS PREVIOUS_MONTH,
	ROUND(
		(
			(
				REVENUE - LAG(REVENUE) OVER (
					ORDER BY
						MONTH
				)
			) / LAG(REVENUE) OVER (
				ORDER BY
					MONTH
			)
		) * 100,
		2
	) AS GROWTH_PERCENTAGE
FROM
	MONTHLY_REVENUE;

--Find Books Contributing to 80% of Total Revenue 
WITH
	BOOK_REVENUE AS (
		SELECT
			B.BOOK_ID,
			B.TITLE,
			SUM(O.TOTAL_AMOUNT) AS REVENUE
		FROM
			ORDERS O
			JOIN BOOKS B ON O.BOOK_ID = B.BOOK_ID
		GROUP BY
			B.BOOK_ID,
			B.TITLE
	),
	RANKED_BOOKS AS (
		SELECT
			*,
			SUM(REVENUE) OVER (
				ORDER BY
					REVENUE DESC
			) AS RUNNING_TOTAL,
			SUM(REVENUE) OVER () AS GRAND_TOTAL
		FROM
			BOOK_REVENUE
	)
SELECT
	*
FROM
	RANKED_BOOKS
WHERE
	RUNNING_TOTAL <= 0.8 * GRAND_TOTAL;

--Identify Customer Purchase Frequency & Retention Category
WITH
	ORDER_COUNTS AS (
		SELECT
			CUSTOMER_ID,
			COUNT(ORDER_ID) AS TOTAL_ORDERS
		FROM
			ORDERS
		GROUP BY
			CUSTOMER_ID
	)
SELECT
	CUSTOMER_ID,
	TOTAL_ORDERS,
	CASE
		WHEN TOTAL_ORDERS = 1 THEN 'One-Time Buyer'
		WHEN TOTAL_ORDERS BETWEEN 2 AND 4  THEN 'Occasional Buyer'
		ELSE 'Frequent Buyer'
	END AS CUSTOMER_CATEGORY
FROM
	ORDER_COUNTS;

--Find Revenue Contribution by Genre With Percentage Share
WITH
	GENRE_REVENUE AS (
		SELECT
			B.GENRE,
			SUM(O.TOTAL_AMOUNT) AS REVENUE
		FROM
			ORDERS O
			JOIN BOOKS B ON O.BOOK_ID = B.BOOK_ID
		GROUP BY
			B.GENRE
	),
	TOTAL_REV AS (
		SELECT
			SUM(REVENUE) AS GRAND_TOTAL
		FROM
			GENRE_REVENUE
	)
SELECT
	G.GENRE,
	G.REVENUE,
	ROUND((G.REVENUE / T.GRAND_TOTAL) * 100, 2) AS PERCENTAGE_SHARE
FROM
	GENRE_REVENUE G
	CROSS JOIN TOTAL_REV T
ORDER BY
	PERCENTAGE_SHARE DESC;

--Detect Declining Sales Books (Trend Analysis)
WITH
	MONTHLY_BOOK_SALES AS (
		SELECT
			B.BOOK_ID,
			B.TITLE,
			DATE_TRUNC('month', O.ORDER_DATE) AS MONTH,
			SUM(O.QUANTITY) AS MONTHLY_SALES
		FROM
			ORDERS O
			JOIN BOOKS B ON O.BOOK_ID = B.BOOK_ID
		GROUP BY
			B.BOOK_ID,
			B.TITLE,
			MONTH
	)
SELECT
	BOOK_ID,
	TITLE,
	MONTH,
	MONTHLY_SALES,
	LAG(MONTHLY_SALES) OVER (
		PARTITION BY
			BOOK_ID
		ORDER BY
			MONTH
	) AS PREVIOUS_MONTH_SALES
FROM
	MONTHLY_BOOK_SALES;

--Identify Customers Whose Spending Is Above Overall Average
WITH
	CUSTOMER_TOTALS AS (
		SELECT
			CUSTOMER_ID,
			SUM(TOTAL_AMOUNT) AS TOTAL_SPENT
		FROM
			ORDERS
		GROUP BY
			CUSTOMER_ID
	)
SELECT
	*
FROM
	CUSTOMER_TOTALS
WHERE
	TOTAL_SPENT > (
		SELECT
			AVG(TOTAL_SPENT)
		FROM
			CUSTOMER_TOTALS
	);

--Calculate Running Total Revenue Over Time
SELECT
	ORDER_DATE,
	SUM(TOTAL_AMOUNT) OVER (
		ORDER BY
			ORDER_DATE ROWS BETWEEN UNBOUNDED PRECEDING
			AND CURRENT ROW
	) AS CUMULATIVE_REVENUE
FROM
	ORDERS
ORDER BY
	ORDER_DATE;

