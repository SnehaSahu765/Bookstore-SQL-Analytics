-- Find the top 3 best-selling books per genre using a Window Function
SELECT
	GENRE,
	TITLE,
	TOTAL_SOLD
FROM
	(
		SELECT
			B.GENRE,
			B.TITLE,
			SUM(O.QUANTITY) AS TOTAL_SOLD,
			RANK() OVER (
				PARTITION BY
					B.GENRE
				ORDER BY
					SUM(O.QUANTITY) DESC
			) AS RANK
		FROM
			BOOKS B
			JOIN ORDERS O ON B.BOOK_ID = O.BOOK_ID
		GROUP BY
			B.GENRE,
			B.TITLE
	) AS RANKED_BOOKS
WHERE
	RANK <= 3;

-- Calculate percentage contribution of each customer to total revenue
WITH
	TOTALREVENUE AS (
		SELECT
			SUM(TOTAL_AMOUNT) AS GRAND_TOTAL
		FROM
			ORDERS
	)
SELECT
	C.NAME,
	SUM(O.TOTAL_AMOUNT) AS CUSTOMER_SPEND,
	ROUND((SUM(O.TOTAL_AMOUNT) / TR.GRAND_TOTAL) * 100, 2) AS CONTRIBUTION_PERCENTAGE
FROM
	CUSTOMERS C
	JOIN ORDERS O ON C.CUSTOMER_ID = O.CUSTOMER_ID
	CROSS JOIN TOTALREVENUE TR
GROUP BY
	C.NAME,
	TR.GRAND_TOTAL
ORDER BY
	CONTRIBUTION_PERCENTAGE DESC;

